# Tipagem estatica
# Quando é necessário informar o tipo da variável

# nome : caracter
nome: str
nome = "Marta"

# idade : inteiro
idade: int
idade = 16

# altura : real
altura: float
altura = 1.87

# Exibir os dados (Saída)
print(f"Nome : {nome}")
print(f"Idade : {idade}")
print(f"Altura : {altura}")